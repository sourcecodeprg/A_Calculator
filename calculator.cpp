#include "calculator.hpp"
float calculator::add(float num1 , float num2){
    return num1+num2;
}

float calculator::sub(float num1, float num2){
    return num1-num2;
}

float calculator::mul(float num1,float num2){
    return num1*num2;
}

float calculator::div(float num1, float num2){
    return num1/num2;
}